jQuery(document).ready(function (e) {
    function t(t) {
        e(t).bind("click", function (t) {
            t.preventDefault();
            e(this).parent().fadeOut()
        })
    }
    e(".dropdownn-toggle").click(function () {
        var t = e(this).parents(".button-dropdownn").children(".dropdownn-menu").is(":hidden");
        e(".button-dropdownn .dropdownn-menu").hide();
        e(".button-dropdownn .dropdownn-toggle").removeClass("active");
        if (t) {
            e(this).parents(".button-dropdownn").children(".dropdownn-menu").toggle().parents(".button-dropdownn").children(".dropdownn-toggle").addClass("active")
        }
    });
    e(document).bind("click", function (t) {
        var n = e(t.target);
        if (!n.parents().hasClass("button-dropdownn")) e(".button-dropdownn .dropdownn-menu").hide();
    });
    e(document).bind("click", function (t) {
        var n = e(t.target);
        if (!n.parents().hasClass("button-dropdownn")) e(".button-dropdownn .dropdownn-toggle").removeClass("active");
    })
});


var modal = document.getElementById("myModal");

// Get the button that opens the modal
var btn = document.getElementById("myBtn");

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close")[0];

// When the user clicks the button, open the modal 
btn.onclick = function() {
  modal.style.display = "block";
}

// When the user clicks on <span> (x), close the modal
span.onclick = function() {
  modal.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
  if (event.target == modal) {
    modal.style.display = "none";
  }
}

